class Calculator:
    """A simple calculator class."""
    
    def add(self, a: float, b: float) -> float:
        """Add two numbers and return their sum."""
        return a + b
    
    def subtract(self, a: float, b: float) -> float:
        """Subtract b from a and return the result."""
        return a - b